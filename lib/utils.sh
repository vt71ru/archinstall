#!/usr/bin/env bash
#
# ArchInstaller
# Utilities module
#

########################################
# Проверка команды
########################################

command_exists()
{
    command -v "$1" >/dev/null 2>&1
}

########################################
# Проверка переменной
########################################

is_set()
{
    [[ -n "${!1:-}" ]]
}

########################################
# Выполнение команды
########################################

run_command()
{
    local message="$1"
    shift

    msg_info "$message"

    if "$@"; then
        msg_success "$message"
        return 0
    fi

    msg_error "$message"
    return 1
}

########################################
# Проверка файла
########################################

file_exists()
{
    [[ -f "$1" ]]
}

########################################
# Проверка каталога
########################################

directory_exists()
{
    [[ -d "$1" ]]
}

########################################
# Создание каталога
########################################

ensure_directory()
{
    mkdir -p "$1"
}

########################################
# Ввод данных
########################################

ask_input()
{
    local prompt="$1"
    local variable="$2"

    read -rp "$prompt: " "$variable"
}

########################################
# Проверка числа
########################################

is_number()
{
    [[ "$1" =~ ^[0-9]+$ ]]
}

########################################
# Проверка сети
########################################

host_available()
{
    ping -c 1 -W 3 "$1" >/dev/null 2>&1
}

########################################
# Удаление переменной
########################################

unset_var()
{
    unset "$1" 2>/dev/null || true
}
