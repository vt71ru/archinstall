#!/usr/bin/env bash
#
# ArchInstaller
# Utilities module
#

########################################
# Проверка команды
########################################

command_exists()
{
    command -v "$1" >/dev/null 2>&1
}

########################################
# Проверка переменной
########################################

is_set()
{
    [[ -n "${!1:-}" ]]
}

########################################
# Ожидание процесса
########################################

wait_for()
{
    local pid="$1"
    local message="${2:-Ожидание}"

    while kill -0 "$pid" >/dev/null 2>&1; do
        progress "$message"
        sleep 1
    done

    progress_done
}

########################################
# Выполнение команды
########################################

run_command()
{
    local message="$1"
    shift

    msg_info "$message"

    if "$@"; then
        msg_success "$message"
        return 0
    fi

    msg_error "Ошибка: $message"
    return 1
}

########################################
# Проверка файла
########################################

file_exists()
{
    [[ -f "$1" ]]
}

########################################
# Проверка каталога
########################################

directory_exists()
{
    [[ -d "$1" ]]
}

########################################
# Создание каталога
########################################

ensure_directory()
{
    local directory="$1"

    if [[ ! -d "$directory" ]]; then
        mkdir -p "$directory"
    fi
}

########################################
# Ввод значения
########################################

ask_input()
{
    local prompt="$1"
    local variable="$2"

    read -rp "$prompt: " "$variable"
}

########################################
# Проверка числа
########################################

is_number()
{
    [[ "$1" =~ ^[0-9]+$ ]]
}

########################################
# Формат размера
########################################

human_size()
{
    local size="$1"

    if command_exists numfmt; then
        numfmt --to=iec "$size"
    else
        echo "${size} bytes"
    fi
}

########################################
# Проверка хоста
########################################

host_available()
{
    local host="$1"

    ping -c 1 -W 3 "$host" >/dev/null 2>&1
}

########################################
# Удаление переменной
########################################

unset_var()
{
    unset "$1" 2>/dev/null || true
}
