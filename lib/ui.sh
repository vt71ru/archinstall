########################################
# Информационные сообщения
########################################

msg_info()
{
    echo -e "${BLUE}[INFO]${RESET} $*"
}

msg_success()
{
    echo -e "${GREEN}[ OK ]${RESET} $*"
}

msg_warning()
{
    echo -e "${YELLOW}[WARN]${RESET} $*"
}

msg_error()
{
    echo -e "${RED}[ERROR]${RESET} $*" >&2
}


########################################
# Оформление интерфейса
########################################
line()
{
    echo "-------------------------------------------------------------------------"
}

section()
{
    echo
    echo -e "${CYAN}========== $* ==========${RESET}"
    echo
}


########################################
# Ожидание пользователя
########################################

press_enter()
{
    read -rp "Нажмите Enter для продолжения..."
}

########################################
# Подтверждение действия
########################################

confirm()
{
    local answer

    while true; do
        read -rp "$1 [y/N]: " answer

        case "$answer" in
            y|Y|yes|YES)
                return 0
                ;;
            n|N|no|NO|"")
                return 1
                ;;
            *)
                msg_warning "Введите y или n"
                ;;
        esac
    done
}

########################################
# Меню выбора
########################################

menu_select()
{
    local title="$1"
    shift

    local options=("$@")
    local choice
    local i

    echo
    echo -e "${CYAN}${title}${RESET}"
    echo

    for i in "${!options[@]}"; do
        echo "$((i+1))) ${options[$i]}"
    done

    echo

    while true; do
        read -rp "Выбор: " choice

        if [[ "$choice" =~ ^[0-9]+$ ]] \
            && (( choice >= 1 && choice <= ${#options[@]} )); then
            REPLY="$choice"
            return 0
        fi

        msg_error "Неверный выбор"
    done
}

########################################
# Индикатор выполнения
########################################

progress()
{
    local message="$1"

    echo -ne "\r${CYAN}[...]${RESET} ${message}"
}

progress_done()
{
    echo -e "\r${GREEN}[ OK ]${RESET}"
}

########################################
# Очистка экрана
########################################

ui_clear()
